function formatPackageFieldsForLine(line: PositionTraceabilityLine): {
  packageCount: string;
  packageType: string;
} {
  const description = line.description?.trim() ?? "";
  if (isPackagingLine(description)) {
    const countMatch = description.match(/\b(\d+)\s*(?:x\s*)?(?:pal|pallet|carton|ctn|box|crate)/i);
    const typeMatch = description.match(/\b(pallet|pal|carton|ctn|box|crate)s?\b/i);
    const typeRaw = typeMatch?.[1]?.toUpperCase() ?? "";
    const type =
      /PAL/.test(typeRaw) ? "PAL" : /CTN|CARTON/.test(typeRaw) ? "CTN" : /BOX/.test(typeRaw) ? "BOX" : /CRATE/.test(typeRaw) ? "CRT" : "";
    return {
      packageCount: countMatch?.[1] ?? "",
      packageType: type,
    };
  }
  const lineType = resolveInvoiceLineType(description);
  if (lineType === "SERVICE") {
    return { packageCount: "", packageType: "" };
  }
  return { packageCount: "", packageType: "" };
}

function formatAggregatedPackageFields(lines: PositionTraceabilityLine[]): {
  packageCount: string;
  packageType: string;
} {
  const parts: string[] = [];
  for (const line of lines) {
    const { packageCount, packageType } = formatPackageFieldsForLine(line);
    if (packageCount && packageType) {
      parts.push(`${packageCount} | ${packageType}`);
    }
  }
  if (parts.length === 0) return { packageCount: "", packageType: "" };
  const unique = [...new Set(parts)];
  if (unique.length === 1) {
    const [count, type] = unique[0]!.split(" | ");
    return { packageCount: count ?? "", packageType: type ?? "" };
  }
  return { packageCount: unique.map((p) => p.split(" | ")[0]).join(", "), packageType: unique.map((p) => p.split(" | ")[1]).join(", ") };
}

